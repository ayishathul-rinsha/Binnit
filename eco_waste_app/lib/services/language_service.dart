enum AppLanguage {
  english('en', 'English', '🇬🇧'),
  hindi('hi', 'हिन्दी', '🇮🇳'),
  malayalam('ml', 'മലയാളം', '🇮🇳'),
  tamil('ta', 'தமிழ்', '🇮🇳'),
  kannada('kn', 'ಕನ್ನಡ', '🇮🇳'),
  telugu('te', 'తెలుగు', '🇮🇳'),
  marathi('mr', 'मराठी', '🇮🇳'),
  bengali('bn', 'বাংলা', '🇮🇳'),
  gujarati('gu', 'ગુજરાતી', '🇮🇳');
  final String code;
  final String nativeName;
  final String flag;
  const AppLanguage(this.code, this.nativeName, this.flag);
}

class LanguageService extends ChangeNotifier {
  static const String _languageKey = 'selected_language';
  static const String _onboardingKey = 'onboarding_completed';
  AppLanguage _currentLanguage = AppLanguage.english;
  bool _onboardingCompleted = false;
  AppLanguage get currentLanguage => _currentLanguage;
  bool get onboardingCompleted => _onboardingCompleted;

  Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    final savedCode = prefs.getString(_languageKey);
    _onboardingCompleted = prefs.getBool(_onboardingKey) ?? false;
    if (savedCode != null) {
      _currentLanguage = AppLanguage.values.firstWhere(
        (lang) => lang.code == savedCode, orElse: () => AppLanguage.english);
    }
  }

  Future<void> setLanguage(AppLanguage language) async {
    _currentLanguage = language;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_languageKey, language.code);
    notifyListeners();
  }

  Future<void> completeOnboarding() async {
    _onboardingCompleted = true;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setBool(_onboardingKey, true);
    notifyListeners();
  }

  String t(String key) => _translations[_currentLanguage.code]?[key] ?? _translations['en']?[key] ?? key;

